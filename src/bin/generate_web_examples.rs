use serde::Deserialize;
use std::collections::HashMap;
use std::fs;
use std::path::PathBuf;

#[derive(Deserialize)]
struct Manifest {
    fixture: Vec<Fixture>,
    #[serde(default)]
    web_example: Vec<WebExample>,
}

#[derive(Deserialize)]
struct Fixture {
    pest: String,
    entry: String,
    stem: String,
}

#[derive(Deserialize)]
struct WebExample {
    key: String,
    stem: String,
    label: String,
    description: String,
    #[serde(default)]
    samples: Vec<String>,
}

fn escape_js_string(s: &str) -> String {
    let mut out = String::from('"');
    for ch in s.chars() {
        match ch {
            '\\' => out.push_str("\\\\"),
            '"' => out.push_str("\\\""),
            '\n' => out.push_str("\\n"),
            '\r' => out.push_str("\\r"),
            '\t' => out.push_str("\\t"),
            other => out.push(other),
        }
    }
    out.push('"');
    out
}

fn escape_js_template(s: &str) -> String {
    s.replace('\\', "\\\\")
        .replace('`', "\\`")
        .replace("${", "\\${")
}

fn comment_header(label: &str, description: &str, entry: &str, samples: &[String]) -> String {
    let mut lines = vec![
        format!("// {} example.", label),
        format!("// {}", description),
        format!("// Entry rule: {entry}"),
    ];
    if !samples.is_empty() {
        let joined = samples
            .iter()
            .map(|sample| sample.replace('\n', "\\n").replace('\r', ""))
            .collect::<Vec<_>>()
            .join(", ");
        lines.push(format!("// Try: {joined}"));
    }
    lines.push(String::new());
    lines.join("\n")
}

fn main() {
    let root = PathBuf::from(env!("CARGO_MANIFEST_DIR"));
    let manifest_src =
        fs::read_to_string(root.join("tests/fixtures.toml")).expect("read tests/fixtures.toml");
    let manifest: Manifest = toml::from_str(&manifest_src).expect("parse tests/fixtures.toml");

    let fixtures_by_stem: HashMap<&str, &Fixture> = manifest
        .fixture
        .iter()
        .map(|fixture| (fixture.stem.as_str(), fixture))
        .collect();

    let mut out = String::from(
        "// Auto-generated by generate-web-examples — do not edit.\n\nexport const EXAMPLES = {\n",
    );

    for web in &manifest.web_example {
        let fixture = fixtures_by_stem.get(web.stem.as_str()).unwrap_or_else(|| {
            panic!(
                "web_example {:?} references unknown stem {:?}",
                web.key, web.stem
            )
        });
        let pest_path = root.join("tests/fixtures").join(&fixture.pest);
        let source = fs::read_to_string(&pest_path)
            .unwrap_or_else(|e| panic!("read {}: {e}", pest_path.display()));
        let pest = format!(
            "{}{}",
            comment_header(&web.label, &web.description, &fixture.entry, &web.samples),
            source
        );

        out.push_str(&format!(
            "  {}: {{\n    syntax: \"pest\",\n    label: {},\n    description: {},\n    entryRule: {},\n    pest: `{}`,\n  }},\n",
            web.key,
            escape_js_string(&web.label),
            escape_js_string(&web.description),
            escape_js_string(&fixture.entry),
            escape_js_template(&pest),
        ));
    }

    struct PegExample {
        key: &'static str,
        label: &'static str,
        description: &'static str,
        entry_rule: &'static str,
        source: &'static str,
    }

    let peg_examples = [
        PegExample {
            key: "peg_hello",
            label: "PEG: hello world",
            description: "Minimal PEG rule with <- and string literal.",
            entry_rule: "main",
            source: "main <- \"hello\"",
        },
        PegExample {
            key: "peg_calc",
            label: "PEG: calculator",
            description: "Choice / sequence, repetition, grouping. (No implicit whitespace.)",
            entry_rule: "expr",
            source: "expr <- term ((\"+\" / \"-\") term)*\nterm <- factor ((\"*\" / \"/\") factor)*\nfactor <- number / \"(\" expr \")\"\nnumber <- [0-9]+",
        },
        PegExample {
            key: "peg_kv",
            label: "PEG: key/value list",
            description: "Tagged binds (name=value), comma-separated list.",
            entry_rule: "main",
            source: "main <- item (\",\" item)*\nitem <- name=ident \"=\" value=number\nident <- [A-Za-z_][A-Za-z0-9_]*\nnumber <- [0-9]+",
        },
    ];

    for peg in &peg_examples {
        let pest = format!(
            "{}{}",
            comment_header(peg.label, peg.description, peg.entry_rule, &[]),
            peg.source
        );
        out.push_str(&format!(
            "  {}: {{\n    syntax: \"peg\",\n    label: {},\n    description: {},\n    entryRule: {},\n    pest: `{}`,\n  }},\n",
            peg.key,
            escape_js_string(peg.label),
            escape_js_string(peg.description),
            escape_js_string(peg.entry_rule),
            escape_js_template(&pest),
        ));
    }

    out.push_str("};\n\n");

    let default_key = manifest
        .web_example
        .first()
        .map(|web| web.key.as_str())
        .unwrap_or("simple");
    out.push_str(&format!(
        "export const DEFAULT_EXAMPLE_KEY = {default_key:?};\nexport const DEFAULT_PEST = EXAMPLES[DEFAULT_EXAMPLE_KEY].pest;\nexport const DEFAULT_PEG = EXAMPLES[\"peg_hello\"].pest;\n"
    ));

    let out_path = root.join("web/src/examples.js");
    fs::write(&out_path, out).expect("write web/src/examples.js");
    println!("updated {}", out_path.display());
}
